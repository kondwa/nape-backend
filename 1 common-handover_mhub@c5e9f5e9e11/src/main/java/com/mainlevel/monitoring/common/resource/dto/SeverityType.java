package com.mainlevel.monitoring.common.resource.dto;

/**
 * Enumeration that holds literals indicating the severity of a message.
 */
public enum SeverityType {

    /**
     * Failure message.
     */
    FAILURE,

    /**
     * Error message.
     */
    ERROR,

    /**
     * Warning message.
     */
    WARNING,

    /**
     * Information message.
     */
    INFO;
}
