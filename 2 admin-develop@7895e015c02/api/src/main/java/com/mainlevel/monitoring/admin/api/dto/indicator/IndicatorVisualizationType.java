package com.mainlevel.monitoring.admin.api.dto.indicator;

/**
 * Different indicator visualization types for indicators.
 */
public enum IndicatorVisualizationType {

    /**
     * Indicator will be visualized as column chart.
     */
    COLUMN_CHART,

    /**
     * Indicator will be visualized as bar chart.
     */
    BAR_CHART,

    /**
     * Indicator will be visualized as line chart.
     */
    LINE_CHART,

    /**
     * Indicator will be visualized as table.
     */
    TABLE,

}
